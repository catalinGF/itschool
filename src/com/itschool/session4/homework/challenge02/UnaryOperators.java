package com.itschool.session4.homework.challenge02;

public class UnaryOperators {
    public static void main(String[] args) {
        int x = 1;

        System.out.println(++x);
        System.out.println(--x);
        System.out.println(x++);
        System.out.println(x--);
        System.out.println(x);
    }
}
