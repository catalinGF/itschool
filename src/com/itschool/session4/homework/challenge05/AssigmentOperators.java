package com.itschool.session4.homework.challenge05;

public class AssigmentOperators {

    public static void main(String[] args) {
        int x = 123;
        int z = 5;

        x *= z;
        System.out.println("x after *= z is " + x);

        //Sum
        x += z;
        System.out.println("x after x +=z is " + x);

        //Substraction
        x -= z;
        System.out.println("x after x -=z is " + x);

        //Division
        x /= z;
        System.out.println("s after x /=z is " + x);
    }
}
